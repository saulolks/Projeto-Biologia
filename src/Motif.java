
public class Motif {
   private int hamdis;
   private int posinic;

public Motif() {
 
}
public Motif(int hamdis, int posinic) {
	this.hamdis = hamdis;
	this.posinic = posinic;
}
public int getHamdis() {
	return hamdis;
}
public void setHamdis(int hamdis) {
	this.hamdis = hamdis;
}
public int getPosinic() {
	return posinic;
}
public void setPosinic(int posinic) {
	this.posinic = posinic;
}
   
}
