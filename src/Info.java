
public class Info {
	
	private int[] a;
	private int i;
	
	public Info(int[] a, int i) {
		super();
		this.a = a;
		this.i = i;
	}

	public int[] getA() {
		return a;
	}

	public void setA(int[] a) {
		this.a = a;
	}

	public int getI() {
		return i;
	}

	public void setI(int i) {
		this.i = i;
	}
	
}
